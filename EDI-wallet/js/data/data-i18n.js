/**
 * Translate a Dutch data string to the current language.
 * If language is 'nl' or string is not found, returns the original.
 */
export function td(str) {
  if (str == null) return str;
  const lang = getLanguage();
  if (lang === 'nl') return str;
  const entry = dataTranslations[str];
  if (!entry) return str;
  return entry[lang] || str;
}
